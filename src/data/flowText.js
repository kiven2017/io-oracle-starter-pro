function interpolate(template, values = {}) {
  return template.replace(/\{(\w+)\}/g, (_, key) => String(values[key] ?? ''))
}

export function getFlowText(locale = 'en') {
  const copy = FLOW_TEXT[locale] ?? FLOW_TEXT.en

  return {
    ...copy,
    interpolate: (template, values) => interpolate(template, values),
  }
}
