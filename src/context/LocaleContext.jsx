function getInitialLocale() {
  if (typeof window === 'undefined') {
    return 'en'
  }

  const userSelected = window.localStorage.getItem(LOCALE_USER_SELECTED_KEY) === '1'
  if (!userSelected) {
    return 'en'
  }

  const stored = window.localStorage.getItem(LOCALE_STORAGE_KEY)
  if (LANGUAGE_OPTIONS.some((option) => option.id === stored)) {
    return stored
  }

  return 'en'
}

const LocaleContext = createContext(null)

export function LocaleProvider({ children }) {
  const [locale, setLocale] = useState(getInitialLocale)
  const setPreferredLocale = useCallback((nextLocale) => {
    setLocale((currentLocale) => {
      const normalizedLocale = LANGUAGE_OPTIONS.some((option) => option.id === nextLocale) ? nextLocale : currentLocale

      if (typeof window !== 'undefined' && normalizedLocale !== currentLocale) {
        window.localStorage.setItem(LOCALE_USER_SELECTED_KEY, '1')
      }

      return normalizedLocale
    })
  }, [])

  const localeInfo = useMemo(
    () => LANGUAGE_OPTIONS.find((option) => option.id === locale) ?? LANGUAGE_OPTIONS[0],
    [locale],
  )
  const content = useMemo(() => buildContent(locale), [locale])

  useEffect(() => {
    if (typeof window === 'undefined') {
      return
    }

    window.localStorage.setItem(LOCALE_STORAGE_KEY, locale)
    document.documentElement.lang = localeInfo.htmlLang
  }, [locale, localeInfo.htmlLang])

  const value = useMemo(
    () => ({
      locale,
      setLocale: setPreferredLocale,
      localeInfo,
      languages: LANGUAGE_OPTIONS,
      content,
      formatText: interpolate,
    }),
    [content, locale, localeInfo, setPreferredLocale],
  )

  return <LocaleContext.Provider value={value}>{children}</LocaleContext.Provider>
}

export function useLocale() {
  const value = useContext(LocaleContext)
  if (!value) {
    throw new Error('useLocale must be used within LocaleProvider')
  }

  return value
}
